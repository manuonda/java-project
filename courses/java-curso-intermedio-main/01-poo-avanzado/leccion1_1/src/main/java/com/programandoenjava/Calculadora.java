package com.programandoenjava;

class Calculadora {
    // Método con un parámetro
    int sumar(int a) {
        return a + 10;
    }

    // Método con dos parámetros
    int sumar(int a, int b) {
        return a + b;
    }

    // Método con diferentes tipos de datos
    double sumar(double a, double b) {
        return a + b;
    }
}